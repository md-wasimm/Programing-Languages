num1 = 10
num2 = 2

# Arithmatic Operators
print("num1 + num2 is:", num1 + num2)
print("num1 - num2 is:", num1 - num2)
print("num1 * num2 is:", num1 * num2)
print("num1 / num2 is:", num1 / num2)
print("num1 // num2 is:", num1 // num2)
print("num1 ** num2 is:", num1 ** num2)
print("num1 % num2 is:", num1 % num2)

